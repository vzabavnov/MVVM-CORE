namespace Zabavnov.MVVM
{
    /// <summary>
    ///     Specify visibility status
    /// </summary>
    public enum Visibility
    {
        /// <summary>
        ///     The control is visible and enabled
        /// </summary>
        Visible,

        /// <summary>
        ///     The control is hidden
        /// </summary>
        Hiden,

        /// <summary>
        ///     The control is visible but disabled
        /// </summary>
        Disabled
    }
}